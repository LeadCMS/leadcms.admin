const LIQUID_CONTROL_KEYWORDS = new Set([
  "if",
  "else",
  "elsif",
  "endif",
  "unless",
  "endunless",
  "for",
  "endfor",
  "case",
  "endcase",
  "when",
  "capture",
  "endcapture",
  "comment",
  "endcomment",
  "raw",
  "endraw",
  "assign",
  "increment",
  "decrement",
  "include",
  "render",
  "break",
  "continue",
  "cycle",
  "tablerow",
  "endtablerow",
]);

/**
 * Extract simple (non-nested) Liquid-style tokens from template source.
 * Skips tokens containing dots (e.g. `account.name`) and Liquid
 * control tags like `if`, `endif`, `for`, `endfor`, `else`, etc.
 */
export function extractTemplateTokens(template: string): string[] {
  const seen = new Set<string>();
  const results: string[] = [];
  const re = /\{\{[-]?\s*([\w.]+)\s*[-]?\}\}/g;
  let m: RegExpExecArray | null;
  while ((m = re.exec(template)) !== null) {
    const token = m[1];
    if (token.includes(".")) continue;
    if (LIQUID_CONTROL_KEYWORDS.has(token.toLowerCase())) continue;
    if (!seen.has(token.toLowerCase())) {
      seen.add(token.toLowerCase());
      results.push(token);
    }
  }
  return results;
}

