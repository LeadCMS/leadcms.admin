const SERVER_KNOWN_LOWER = new Set([...SERVER_KNOWN_PARAMS].map((p) => p.toLowerCase()));

/**
 * Extract simple (non-nested) Liquid-style tokens from template source.
 * Skips tokens containing dots (e.g. `account.name`) and Liquid
 * control tags like `if`, `endif`, `for`, `endfor`, `else`, etc.
 */
function extractTemplateTokens(template: string): string[] {
  const seen = new Set<string>();
  const results: string[] = [];
  const controlKeywords = new Set([
    "if",
    "else",
    "elsif",
    "endif",
    "unless",
    "endunless",
    "for",
    "endfor",
    "case",
    "endcase",
    "when",
    "capture",
    "endcapture",
    "comment",
    "endcomment",
    "raw",
    "endraw",
    "assign",
    "increment",
    "decrement",
    "include",
    "render",
    "break",
    "continue",
    "cycle",
    "tablerow",
    "endtablerow",
  ]);

  // Match {{ token }} and also {%- / {%
  const re = /\{\{[\-]?\s*([\w.]+)\s*[\-]?\}\}/g;
  let m: RegExpExecArray | null;
  while ((m = re.exec(template)) !== null) {
    const token = m[1];
    // Skip nested (dotted) tokens
    if (token.includes(".")) continue;
    // Skip Liquid control keywords
    if (controlKeywords.has(token.toLowerCase())) continue;
    const key = token;
    if (!seen.has(key.toLowerCase())) {
      seen.add(key.toLowerCase());
      results.push(key);
    }
  }
  return results;
}

/**
 * Best-guess a sample value for a custom template parameter
 * based on its name.
 */
function guessParamValue(name: string): string {
  const lower = name.toLowerCase();

  if (lower.includes("email")) return "customer@example.com";
  if (lower.includes("name") && lower.includes("customer")) return "Alex Johnson";
  if (lower.includes("name")) return "Sample Name";
  if (lower.includes("phone") || lower.includes("tel")) return "+1-555-0100";
  if (lower.includes("date")) return "2026-01-15";
  if (lower.includes("url") || lower.includes("link")) return "https://example.com";
  if (lower.includes("amount") || lower.includes("total")) return "99.00";
  if (lower.includes("price") || lower.includes("fee")) return "19.99";
  if (lower.includes("currency")) return "USD";
  if (lower.includes("quantity") || lower.includes("qty")) return "1";
  if (lower.includes("id") || lower.includes("number")) return "12345";
  if (lower.includes("ref")) return "REF-001";
  if (lower.includes("code") || lower.includes("key")) return "XXXX-XXXX-XXXX";
  if (lower.includes("product")) return "Sample Product";
  if (lower.includes("source") || lower.includes("platform")) return "Web";
  if (lower.includes("order")) return "ORD-001";
  if (lower.includes("status")) return "Active";
  if (lower.includes("company") || lower.includes("org")) return "Acme Inc.";
  if (lower.includes("address")) return "123 Main Street";
  if (lower.includes("city")) return "New York";
  if (lower.includes("state")) return "NY";
  if (lower.includes("zip") || lower.includes("postal")) return "10001";
  if (lower.includes("country")) return "US";
  if (lower.includes("title")) return "Sample Title";
  if (lower.includes("description") || lower.includes("desc")) return "Sample description text";
  if (lower.includes("message") || lower.includes("note")) return "Sample message";
  if (lower.includes("license")) return "LIC-XXXX-XXXX";

  return `Sample ${name}`;
}

const CUSTOM_PARAMS_STORAGE_KEY = "email-tpl-preview-custom-params";
const DATA_MODE_STORAGE_KEY = "email-tpl-preview-data-mode";
const CONTACT_TYPE_STORAGE_KEY = "email-tpl-preview-contact-type";
const SELECTED_CONTACT_STORAGE_KEY = "email-tpl-preview-selected-contact";

function loadStoredCustomParams(): CustomParam[] {
  try {
    const raw = localStorage.getItem(CUSTOM_PARAMS_STORAGE_KEY);
    return raw ? JSON.parse(raw) : [];
  } catch {
    return [];
  }
}

function saveStoredCustomParams(params: CustomParam[]) {
  try {
    localStorage.setItem(CUSTOM_PARAMS_STORAGE_KEY, JSON.stringify(params));
  } catch {
    /* quota exceeded */
  }
}

function loadStoredDataMode(): PreviewDataMode {
  try {
    const v = localStorage.getItem(DATA_MODE_STORAGE_KEY);
    return v === "contact" ? "contact" : "dummy";
  } catch {
    return "dummy";
  }
}

function loadStoredContactType(): ContactType {
  try {
    const v = localStorage.getItem(CONTACT_TYPE_STORAGE_KEY);
    if (v === "Full" || v === "Standard" || v === "Basic" || v === "Minimal") return v;
    return "Full";
  } catch {
    return "Full";
  }
}

function loadStoredSelectedContact(): ContactDetailsDto | null {
  try {
    const raw = localStorage.getItem(SELECTED_CONTACT_STORAGE_KEY);
    return raw ? JSON.parse(raw) : null;
  } catch {
    return null;
  }
}

