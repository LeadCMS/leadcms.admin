/**
 * Normalise legacy backend template tokens to Liquid syntax.
 */
export function normalizePlaceholders(source: string): string {
  let result = source;
  result = result.replace(/<%\s*(\w+)\s*%>/g, (_, k) => `{{ ${k} }}`);
  result = result.replace(/&lt;%\s*(\w+)\s*%&gt;/g, (_, k) => `{{ ${k} }}`);
  result = result.replace(/\$\{\s*(\w+)\s*\}/g, (_, k) => `{{ ${k} }}`);
  return result;
}

